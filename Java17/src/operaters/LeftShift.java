package operaters;

public class LeftShift {
	public static void main(String[] args) {
		int a = -7;
		int b = a << 2;
		System.out.println(b);

		int e = -19;
		int c = e << 2;
		System.out.println(c);

		int d = -57;
		int v = d << 2;
		System.out.println(v);

		int t = -90;
		int g = t << 2;
		System.out.println(t);

	}

}

package operaters;

public class BitWiseOperator {
	public static void main(String[] args) {
		System.out.println(4 & 5);
		System.out.println(4 | 5);
		System.out.println(4 ^ 5);
		System.out.println(643644 & 634543287);
		System.out.println(2636632 | 365253);
		System.out.println(46733 ^ 674634);

	}

}

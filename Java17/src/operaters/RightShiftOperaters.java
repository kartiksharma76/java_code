package operaters;

public class RightShiftOperaters {
	public static void main(String[] args) {
		int a = -4;
		int b = a >> 2;
		System.out.println(b);
		int c = 37;
		int d = c >> 2;
		System.out.println(d);
		int w = 7;
		int r = w >> 3;
		System.out.println(r);
		int y = 45;
		int g = y >> 2;
		System.out.println(g);
		int j = -67;
		int x = j >> 2;
		System.out.println(x);
	}

}

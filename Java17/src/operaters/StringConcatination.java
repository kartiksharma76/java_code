package operaters;

public class StringConcatination {
	public static void main(String[] args) {
		System.out.println("Hello" + "Java");
		System.out.println(10 + 20 + "kartik");
		System.out.println("prince" + 10 + 20 + "kartik");
		System.out.println(10 + 20 + "kartik" + 30 + 40);
		System.out.println(20 + "harsh" + 38 + 40);
		System.out.println(20 + 20 + 20 + "jamal" + 20);
		System.out.println("hellopapa" + 90 + 10);
		System.out.println(303030 + 9999 + "kaushal" + 73823 + "kartik" + 637621 + "jamal" + 3678 + "harsh");
		System.out.println(88444);
		System.out.println("kartiK");
		System.out.println("jamal");
		System.out.println("kaushal");
		System.out.println("harsh");

	}
}

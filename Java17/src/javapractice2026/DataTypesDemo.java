package javapractice2026;

public class DataTypesDemo {
	public static void main(String[] args) {

		byte a = 10;
		short b = 100;
		int c = 1000;
		long d = 100000L;

		float e = 10.5f;
		double f = 99.99;

		char g = 'A';
		boolean h = true;

		String name = "Kartik";

		System.out.println(a);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(name);

	}
}

package TraningAdPlacement;

public class ControlStructure {
	public static void main(String[] args) {
		int time = 11;
		if (time < 10) {
			System.out.println("color is red");
		} else if (time < 20) {
			System.out.println("colour is green");

		} else {
			System.out.println("colour is blue");
		}
	}
}
// if time is greater than 20 then output is color is blue
// if time is less than 10 then output is color is red
// if time is greater than 10 and less then 20 output is color is green
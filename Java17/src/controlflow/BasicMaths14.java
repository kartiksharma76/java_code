package controlflow;

public class BasicMaths14 {
	public static void main(String[] args) {
		for (int table = 1; table <= 100; table++) {
			for (int i = 1; i <= 10; i++) {
				System.out.println(table + "*" + (i) + "=" + (table * (i)));
			}
			System.out.println("-----------------------");

		}
	}
}
